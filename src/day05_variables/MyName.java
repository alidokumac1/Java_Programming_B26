package day05_variables;

public class MyName {

    public static void main(String[] args) {


        char firstname1 = 'A';
        char firstname2 = 'L';
        char firstname3 = 'I';

        System.out.println(firstname1);
        System.out.println(firstname2);
        System.out.println(firstname3);

        System.out.println("My name is : " + firstname1 + firstname2 + firstname3);

        // or you can make a variable of the name

        String name = "" + firstname1 +firstname2 + firstname3;
        System.out.println("My name: " + name);





    }
}
