package day04_variables;

public class PrintDate {

    public static void main(String[] args) {





        int month = 8;
        int day = 22;
        int year = 1993;

        System.out.println( month + "/" + day + "/" + year);



        month = 5;

        System.out.println( month + "/" + day + "/" + year);

        day = 3;
        System.out.println( month + "/" + day + "/" + year);

    }



}










