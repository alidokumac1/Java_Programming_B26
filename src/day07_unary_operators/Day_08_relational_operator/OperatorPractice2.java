package day07_unary_operators.Day_08_relational_operator;

public class OperatorPractice2 {
    public static void main(String[] args) {


        int a = 40; // a = 40 ->  39 -> 38 - > 37 -> 36 ->35

        System.out.println(--a); // pre decrement, subtract 1 right away, then it prints the value of a, which is 39
        System.out.println(--a);// 38
        System.out.println(a--);// post decrement,
        System.out.println(--a); //pre decrement
        System.out.println(--a);
        System.out.println(a--);













    }
}
