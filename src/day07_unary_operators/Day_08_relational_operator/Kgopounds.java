package day07_unary_operators.Day_08_relational_operator;

public class Kgopounds {

    public static void main(String[] args) {

     int kg=  10;
     double pound = kg * 2.20462262;

        System.out.println("10 kg equal to " + pound + " pounds");
        System.out.println(2 * kg + "kg equal to " + 2 * pound + " pounds");



        int gallon = 10;
        double litres = gallon * 3.78541;

        System.out.println(gallon + " gallon equal to "+ litres + " litres");
        System.out.println(2 * gallon + " gallon = " + 2 * litres);
    }
}
